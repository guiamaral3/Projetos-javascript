//for
for (var i = 0; i <= 10; i++) {
    console.log(i);
}

//while
console.log("\niniciando while")
var numero = 8;
while (numero <= 10) {
    console.log(numero);
    numero++;
}

//do-while
console.log("\niniciando do while")
var cont = 10;
do {
    console.log('Imprime o valor de cont: ' + cont);
    cont++;
} while (cont <= 5);