// Atribuir e exibir
var num1 = 10, num2 = 20, nome = 'Geek';
console.log(num1, num2, nome);

// Soma
var res = num1 + num2;
console.log("O resultado é: " + res);

// Soma entre inteiro e string faz a concatenação
var num3 = "39";
res = num1 + num3;
console.log(res);

// Subtração
res = num1 - num2;
console.log(res);

// Multiplicação
res = num1 * num2;
console.log(res);

// Divisão
res = num1 / 2;
console.log(res);

// Módulo (resto da divisão)
res = num1 % 2;
console.log(res);

// Incremento/Decremento
num1 += 8; // -=, *=, /= 
console.log(num1);

console.log(num1++); //Imprime a variável e depois incrementa 1
console.log(num1--); //Imprime a variável e depois decrementa 1
console.log(++num1); //Incrementa e depois imprime. 
console.log(--num1); //Decrementa e depois imprime.