const bodyElement = document.querySelector('body') // Colocando o elemento body em uma variavel

// Verde
document.querySelector('#Verde').onclick = function(){
    bodyElement.style.backgroundColor = 'green';
}

// Amarelo
document.querySelector('#Amarelo').onclick = function(){
    bodyElement.style.backgroundColor = 'yellow';
}

// Azul
document.querySelector('#Azul').onclick = function(){
    bodyElement.style.backgroundColor = 'blue';
}

// Roxo
document.querySelector('#Roxo').onclick = function(){
    bodyElement.style.backgroundColor = 'blueviolet';
}

// Rosa
document.querySelector('#Rosa').onclick = function(){
    bodyElement.style.backgroundColor = 'pink';
}